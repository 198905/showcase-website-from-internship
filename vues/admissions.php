        <h5>Le déroulement </h5>
        <p>
            L’audition se déroule sous forme de stage encadré par des professeurs de l’Académie des Arts de Minsk. Votre esprit doit être impérativement disponible lors des deux après-midis. Nous allons entre autre juger votre implication et votre motivation. La Masterclass à Minsk exige beaucoup de travail et demande un grand investissement de la part de chacun. Cette implication doit être valable autant lors des cours que au cottage où vous serez amené à faire la cuisine et à participer aux taches ménagères. Nous évaluerons votre capacité d’intégration au sein d’un groupe. L’audition ne se déroulera pas de manière classique. Certains exercices s’effectueront en groupe. Pendant les exercices vous devrez être capable de montrer votre passion pour le Théâtre, votre envie de vous perfectionner, votre personnalité et votre capacité à intégrer les remarques des professeurs. 
            Vous devrez donc préparer au préalable quelques exercices. Chaque exercice ne doit pas dépasser 3 minutes : 
        </p>
        <ul>
            <li>Une chanson</li>
            <li>Étude "Plastique/ Danse" : chorégraphie </li>
            <li>Étude "Un animal" : il faut incarner un animal, vous devez imaginer une situation où votre comportement évolue à cause d’un événement. Mettre en valeur le caractère psychologique de l’animal </li>
            <li>Étude "Un objet" : en incarnant un objet, vous devez imaginer une situation où votre comportement évolue à cause d’un événement. Mettre en valeur le caractère psychologique de l’obje</li>
        </ul></br>
