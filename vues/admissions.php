        <h5>Pendant le stage à Minsk </h5>
        <p>
            Votre esprit doit être impérativement disponible. Nous aurons besoin de votre implication et de votre motivation. Le stage exige beaucoup de travail et demande un grand investissement de la part de chacun. Cette implication doit être valable autant lors des cours, qu’au sein des appartements et du théâtre. Vous serez donc amener à participer activement à la bonne maintenance des lieux et à vous impliquer fortement dans la vie collective. 
