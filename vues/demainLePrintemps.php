        <img src="" alt="">
        <p>
            « L’École Théâtrale Russe » est une approche de l’art de 
            l’acteur qui a été initiée, à la fin du XIXème siècle, 
            par un courant novateur : « Être acteur, c’est développer en 
            permanence sa fibre artistique, sa sensibilité, son humanisme avec 
            un esprit citoyen ». Vladimir Nemirovitch-Dantchenko et 
            Konstantin Stanislavski sont à l’origine de l’École Théâtrale Russe. 
        </p>
        <p>
            La formation du comédien est fondée sur l’enseignement d’une technique,
            mais aussi sur le développement, chez l’acteur, d’une attitude face à son travail. Une éthique de vie et de travail, basée sur le respect de son partenaire et de son public, un développement de l’imagination et de la curiosité pour faire de l’acteur avant tout un artiste dans l’âme. 
            </br>Le metteur en scène et pédagogue russe Konstantin Stanislavski a, en effet, ajouté à l’apprentissage technique, une transformation de l’individu en tant que personne, connectée à un métier qui exige un travail permanent sur soi, « car l’acteur travaille avec son corps, son esprit, ses propres sentiments et émotions ».  <br><br> 
