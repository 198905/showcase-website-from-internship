    <h2>Les Objectifs</h2>
    <p>
        La masterclass est une formation intensive et pluridisciplinaire de 9 mois (d’octobre à juin). Notre objectif est de permettre à chaque participant d’acquérir de solides bases et techniques du comédien pour construire plus précisément ses « outils » d’acteur et d’aller plus loin dans le processus de travail au sein de l’école théâtrale Russe.</br>
        Bien que la compréhension de la langue russe ne soit pas requise (car les 1800 heures de cours sont traduites simultanément du russe vers le français), pendant la masterclass il est possible d’étudier la culture russe et européenne et, pour les plus motivés, d’apprendre le Russe.</br> 

    </p>
    <h2>Les disciplines enseignées</h2>
    <p>
        Techniques du comédien (avec deux intervenants), biomécanique, diction, danse classique, chant individuel et chant collectif, acrobatie - plastique, piano, combat/escrime/jonglage, tempo-rythme, claquettes, maquillage et cours de russe.</br> 
    </p>
    <h2>Organisation et déroulement de la formation </h2>
    <p>
        La formation se déroule en deux phases successives. Dans une première phase, sont proposés aux participants des travaux sur la notion : « De l’Acte au Sentiment » : une mobilisation vraie et des actes justes font naturellement naître, pendant le processus du jeu, des sentiments et des sensations sincères chez l’acteur. La méthode de travail est basée, d’une part, sur la recherche des procédés qui doivent aider l’acteur, dans son jeu, à éprouver des sentiments et des sensations vrais et, d’autre part, sur l’analyse du processus «psycho-physiologique» qui se produit sur scène. À cet effet, différents modules et exercices sont proposés au comédien stagiaire afin de lui permettre de mettre en pratique toutes les étapes du processus de construction d’un personnage.
    </p>
