        <h1 class="contenuPedagogiqueTitre">En quoi consiste le projet ?</h1>
            <p>Notre association propose une formation artistique intensive et pluridisciplinaire 
                centrée sur l'art de l’acteur telle qu’elle est enseignée dans l’École Théâtrale Russe. En Biélorussie, chaque participant aura ainsi l'opportunité de découvrir un nouvel art de vivre. Nous souhaitons réunir des citoyens responsables, chacun avec son parcours personnel, afin d’enrichir un projet artistique ambitieux. 
                Pour ce faire, nous proposons deux types de formations : 
                <br><br>
                <strong>LE STAGE INTENSIF</strong> : Découverte de l’École Théâtrale Russe pendant 4 semaines (50h/semaine) pendant le mois d’août au théâtre RTBD. 
                
                    <br>
                <strong>LA MASTERCLASS</strong> : Rencontre avec l’École Théâtrale Russe pendant 9 mois de formation intensive (1800h de cours), d’octobre à juin, dans divers théâtres minskois. 
                <br>
            </p>
        <h1 class="contenuPedagogiqueTitre">L’immersion créatrice </h1>
            <p>
                Le stage se déroule en immersion totale afin de comprendre complètement l'École Théâtrale Russe comme si les participants étaient de vrais élèves russes ou biélorusses ! 

                Cette école attache une attention particulière aux conditions 
                de travail et d’exigence qui permettent la réalisation de cet art. 
                Ainsi, cette approche est vécue comme une rencontre artistique mais 
                aussi comme une expérience humaine à travers la découverte d’un pays, 
                une culture, d'une vie collective basée sur le partage et la solidarité, 
                ec des valeurs d'exigence, de rigueur et de persévérance dans le travail. 
            </p>
        <h1 class="contenuPedagogiqueTitre">Une formation pluridisciplinaire </h1>
            <p>
                Notre objectif est d’offrir aux stagiaires une formation 
                intensive et multidisciplinaire. Bien qu’essentiellement centrée 
                sur les techniques du comédien, cette formation intègre également 
                diverses disciplines nécessaires à la formation du comédien. 
                En effet, l’art de l’acteur, tel qu’il se conçoit dans 
                « l’École Théâtrale Russe », fait 
                appel non seulement à l’imagination, mais aussi au corps et à la 
                voix du comédien, comme des instruments que ce dernier doit savoir maîtriser pour exprimer avec précision ses émotions. Ces disciplines sont abordées non pas comme des « spécialités », mais comme des « outils » au service de l’acteur pour l’aider à développer et à aiguiser son art.
                « Tous les artistes ont un instrument : le pianiste à son piano, le peintre à son pinceau, le chanteur est soutenu par la musique. Le comédien n’a que lui-même : son corps est son instrument. » 
                Igor BLINKOV (Professeur de Technique du comédien) 
            </p>
        <h1 class="contenuPedagogiqueTitre">Le concert théâtral</h1>
        <p>
            Le concert théâtral est un genre très reconnu dans les pays de l'est. Il s'agit d'un travail d'études qui regroupe les meilleurs travaux des élèves toutes disciplines confondues soutenant la pluridisciplinarité dans la formation des comédiens. Il présente tout le savoir-faire que les élèves ont accumulé après leur formation : le chant, la danse,… et bien évidemment, l’art théâtral. Ainsi, pour chacune de nos propositions, un concert théâtral ponctue nos formations. 
        </p>
        <h1 class="contenuPedagogiqueTitre">Les participants </h1>
        <p>
            Le projet s’adresse aux acteurs de cinéma et de théâtre, apprentis ou professionnels, ayant au moins 18 ans, désireux de se former à travers une approche différente du théâtre et découvrir « l’École Théâtrale Russe », telle qu’elle est enseignée en Biélorussie, selon les principes et les méthodes de Stanislavsky, Michael Chekhov, Meyerhold et Tovstonogov. Nos formations sont, avant tout, destinées à toute personne capable de remettre en question ses pratiques scéniques afin de nourrir son parcours. 
        </p>
        <h1 class="contenuPedagogiqueTitre">Il n’est pas nécessaire de parler le russe</h1>
        <p>
            Tous les cours sont traduits simultanément en français par quatre interprètes. En enlevant la barrière de la langue par la traduction il est donc possible de suivre les cours des professeurs comme un élève biélorusse, pour que l'expérience soit complète il faut essayer de comprendre l'atmosphère qui règne en classe car l'éthique de travail donne le ton et le rythme de la formation. 
        </p>
